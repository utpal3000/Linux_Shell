#
# commands used for 'comm' command and diff command
# comm commdata1.dat commdata2.dat
# comm -12 commdata1.dat commdata2.dat -- Displays only common names
# comm -23 commdata1.dat commdata2.dat -- Displays only unique names in the first file
# comm -2 commdata1.dat commdata2.dat -- Displays only unique names in the first file and common names in both
#
# diff commdata1.dat commdata2.dat -- Displays differenes in contents of both the files
# diff commdata1.dat commdata1.dat -- Displays no differenes in contents of both the files




